def parse(filename: str):
    with open(filename, "r") as f:
        lines: List[str] = f.read().strip().split("\n")

    return lines[0]


def run(lines, p2=False):
    valid = 0
    buff = deque([])
    for i, c in enumerate(lines):
        buff.append(c)
        if len(set(buff)) == (14 if p2 else 4):
            break
        if i > (12 if p2 else 2):
            buff.popleft()
    return i + 1


